    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double total = 0;
        double adicionais = 0;
        double subtotal = 0;
        
        
        if(!(xBaconRad.isSelected() && xSaladaRad.isSelected() && xPicanhaRad.isSelected())) {
            int showConfirmDialog = JOptionPane.showConfirmDialog(rootPane, btnSair);
        } else {
            if(xBaconRad.isSelected()) {
            total += 25.00;
            subtotal += 25.00;
        } else if(xSaladaRad.isSelected()) {
            total += 17.00;
            subtotal += 17.00;
        } else if(xPicanhaRad.isSelected()) {
            total += 30.00;
            subtotal += 30.00;
        }
        
        if(baconCheck.isSelected()) {
            total += 3.00;
            adicionais += 3.00;
        } 
        if (queijoCheck.isSelected()) {
            total += 2.00;
            adicionais +=2.00;
        } 
        if(catupiryCheck.isSelected()) {
            total += 2.50;
            adicionais += 2.50;
        }
        
        txtTotal.setText(Double.toString(total));
        txtAdicionais.setText(Double.toString(adicionais));
        txtSubTotal.setText(Double.toString(subtotal));
        }
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

