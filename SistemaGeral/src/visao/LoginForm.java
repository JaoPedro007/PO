    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String login =tfUsuario.getText();
        String senha = String.valueOf(pfSenha.getPassword());
        if(login.length()>0 && senha.length()>0){
            UsuarioDao usuarioDao = new UsuarioDao();
            try {
                Usuario usuario = usuarioDao.login(login, senha);
                if(usuario==null){
                    JOptionPane.showMessageDialog(null, "Login ou senha invalidos","Atenção", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                PrincipalForm principal = new PrincipalForm(usuario);
                principal.setVisible(true);
                this.dispose();
               
               
            
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Erro ao realizar consulta no banco de dados ","Erro",JOptionPane.ERROR_MESSAGE);
            }
            
            
        }
        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

