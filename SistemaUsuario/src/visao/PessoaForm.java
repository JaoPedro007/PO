    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        String nome = tf_nome.getText();
        String sexo = GroupSexo.getSelection().toString();
        String endereco = tf_endereco.getText();
        String cep = tf_cep.getText();
        String bairro = tf_cpf.getText();
        int numero=0;
        int codigo=0;
        
        try {
            numero = Integer.parseInt(tf_cep.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "N√∫mero n√£o informado", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Pessoa pessoa = new Pessoa(endereco, cep, bairro, numero, codigo);
        
        try {
            pessoaDao.adicionar(pessoa);
            JOptionPane.showMessageDialog(null, "Pessoa cadastrado com sucesso");
            tf_nome.setText("");
            tf_endereco.setText("");
            tf_cep.setText("");
            
       } catch (SQLException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel cadastrar", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

