    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String login = tf_login.getText();
        String senha = String.valueOf(tf_senha.getPassword());

        if (login.length() > 0 && senha.length() > 0) {
            UsuarioDao usuariodao = new UsuarioDao();

            try {
               Usuario usuario = usuariodao.login(login, senha);
                if (usuario==null) {
                    JOptionPane.showMessageDialog(null, "Login ou senha inválidos", "Atenção", JOptionPane.ERROR_MESSAGE);       
                    return;
                    
                }
                this.dispose();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao realizar consulta no banco", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btn_loginActionPerformed

